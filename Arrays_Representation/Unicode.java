package Arrays_Representation;

public class Unicode {
    public static void main(String[] args) {
        for (char c = 97; c <= 122; c++) {
            System.out.print(c +" ");
        }
        System.out.println("\n- - - - - - -- --- -  -");
        for (char x = 65; x <= 90; x++){
            System.out.print(x + " ");
        }


    }
}
